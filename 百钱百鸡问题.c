#include <stdio.h>
int main()
{
	int x,y,z;
	for(x=0 ;x<=20 ;x++)                       //从公鸡角度切入，一只公鸡五块钱，所以最多20只公鸡 
	{
		for(y=0 ;y<=33 ;y++)                   //再考虑母鸡，一只母鸡三块钱，所以最多33只母鸡 
	    {
	    	z = 100-x-y;                       //最后考虑小鸡，同样从数量角度考虑 
	    	if(5*x+3*y+1.0/3*z==100)           //从钱的角度考虑满足题目的条件 
	    	    printf("%d %d %d\n",x,y,z);    //一旦有x，y，z符合，则立即输出 
		}
	 } 
	return 0;
 } 
